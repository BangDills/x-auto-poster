def parse_metric_text(text):
    if not text:
        return 0
    match = re.search(r'([\d.,]+)\s*([KM]?)', text, re.IGNORECASE)
    if not match:
        return 0
    num_str = match.group(1).replace(',', '')
    unit = match.group(2).upper()
    try:
        val = float(num_str)
        if unit == 'K':
            return int(val * 1000)
        elif unit == 'M':
            return int(val * 1000000)
        return int(val)
    except ValueError:
        return 0

